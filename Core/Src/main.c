void StartDefaultTask(void *argument)
{
	for(;;)
	  {
	    if (telemetry.bms_online)
	    {
	      BMS_Service_ProcessData();
	      uint32_t current_time = HAL_GetTick();
	      uint32_t delta_time = (current_time - last_time) / 1000;
	      BMS_Service_UpdateCounters(delta_time);
	      BMS_Service_HandleLowPowerCondition(&low_power_mode);
	      BMS_Service_HandleFlashStorage();
	      last_time = current_time;

	    }
	    if (low_power_mode)
	    {
	      BMS_Service_HandleLowPowerMode(&low_power_mode);
	    }
	    Log_Telemetry(BMS_MSG_LEVEL_DEBUG, &telemetry);
	    osDelay(100);
  }
}

void StartTask02(void *argument)
{
  Temperature_Init();
  for(;;)
  {
	HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET);
    float temp1 = TMP100_ReadTemperature(&hi2c1, TMP100_IC1_ADDR);
    float temp2 = TMP100_ReadTemperature(&hi2c2, TMP100_IC2_ADDR);
    Log_Message(BMS_MSG_LEVEL_DEBUG, "Temperature Readings:\r\n");
    Log_Message(BMS_MSG_LEVEL_DEBUG, "IC1: %.2f  degC\r\n", temp1);
    Log_Message(BMS_MSG_LEVEL_DEBUG, "IC2: %.2f degC\r\n\r\n", temp2);
    PID_Control(temp1, temp2);

    osDelay(100);
  }
}

void StartTask03(void *argument)
{
  for(;;)
  {
    if (telemetry.bms_online)
    {
      bool success = EnableBalanceCell(&bms_instance1, telemetry.current_ic1 / 1000.0f);
      if (!success)
      {
        // If balancing fails on BMS 1, try BMS 2
        EnableBalanceCell(&bms_instance2, telemetry.current_ic2 / 1000.0f);
      }
    }
    osDelay(100); // delay to avoid tight loop
  }
}

